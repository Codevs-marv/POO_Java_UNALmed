    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // TODO add your handling code here:
        double HorasTrabajadas, ValorHora, ReteFuente, SalBruto, SalNeto, ReteF;
        
        HorasTrabajadas = Double.parseDouble(txtHorasTrabajadas.getText());
        ValorHora = Double.parseDouble(txtValorHora.getText());
        ReteFuente = Double.parseDouble(txtReteFuente.getText());
        
        SalBruto = HorasTrabajadas * ValorHora;
        ReteF = (SalBruto * ReteFuente) / 100;
        SalNeto = SalBruto - ReteF;
        
        txtSalarioBruto.setText(String.valueOf(SalBruto));
        txtSalarioNeto.setText(String.valueOf(SalNeto));
        
    }//GEN-LAST:event_btnCalcularActionPerformed

